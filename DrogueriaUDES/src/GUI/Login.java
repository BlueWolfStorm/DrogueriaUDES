    public void validarUsuario(){
        boolean resultado = false;
        String pass = String.valueOf(txtPass.getPassword());
        String usr = txtDocumento.getText();
        String SQL_Adm ="SELECT `id_adm`, `pass_adm` FROM `admin` WHERE `id_adm`='"+usr+"' AND `pass_adm`='"+pass+"'";
        String SQL_Practicante ="SELECT `id_practicante`,`pass_practicante` FROM `practicante` WHERE `id_practicante`='"+usr+"' AND `pass_practicante` ='"+pass+"'";
        String SQL_Encargado ="SELECT `id_encargado`, `pass_encargado` FROM `encargado` WHERE `id_encargado`='"+usr+"' AND `pass_encargado` ='"+pass+"'";
        
        try {
            Statement st_adm = con.createStatement();
            ResultSet rs_adm = st_adm.executeQuery(SQL_Adm);
            
            if (rs_adm.next()) {
                resultado = true;
                if (resultado == true) {
                    InterfazAdm adm = new InterfazAdm();
                    adm.setVisible(true);
                    this.dispose();
                }
            
            
            }else{
                
                        try {
                            Statement st_encargado = con.createStatement();
                            ResultSet rs_encargado = st_encargado.executeQuery(SQL_Encargado);

                            if (rs_encargado.next()) {
                                resultado = true;
                                if (resultado == true) {
                                    JOptionPane.showMessageDialog(null, "Encargado Logueado");
                                  /*  InterfazAdm adm = new InterfazAdm();
                                    adm.setVisible(true);
                                    this.dispose(); */
                                }


                            }else{
                                
                                        try {
                                            Statement st_practicante = con.createStatement();
                                            ResultSet rs_practicante = st_practicante.executeQuery(SQL_Practicante);

                                            if (rs_practicante.next()) {
                                                resultado = true;
                                                if (resultado == true) {
                                                    JOptionPane.showMessageDialog(null, "Practicante Logueado");
                                                   /* InterfazAdm adm = new InterfazAdm();
                                                    adm.setVisible(true);
                                                    this.dispose();*/
                                                }


                                            }else{
                                                JOptionPane.showMessageDialog(null, "Usuario no Registrado");
                                            }
                                        } catch (Exception e) {
                                            JOptionPane.showMessageDialog(null, "Error "+e.getMessage());
                                        }
                                
                            }
                        } catch (Exception e) {
                            JOptionPane.showMessageDialog(null, "Error "+e.getMessage());
                        }
                
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error "+e.getMessage());
        }
    }
    
